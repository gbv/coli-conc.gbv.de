**coli-ana** is a tool for analysis of synthesized DDC numbers. The tool is
being developed as part of project colibri which gave birth to project
coli-conc. We are going to provide information about decomposed DDC number in
different formats (MARC, JSKOS...). The results of this tool present a great
value for subject-indexers, mapping works and for the research community.
